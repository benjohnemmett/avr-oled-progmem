void DrawBitMap(Framebuffer &fb, uint8_t x, uint8_t y) {
  for (int r = 0; r < 32; r++) {
    for (int c = 0; c < 32; c++) {
      fb.drawPixel(c + x, r + y, pgm_read_byte(&smile[r][c]));
    }
  }
}

int8_t dx = 1;
int8_t dy = 2;

int main(void) {
  Framebuffer fb;
  
  DDRB |= (1 << PB5);

  while (1) {
    PORTB ^= (1 << PB5);

    fb.clear();

    if (_x1 <= 1) {
      dx = 2;
    } else if (_x1 > (128 - 32)) {
      dx = -2;
    }

    if (_y1 <= 0) {
      dy = 1;
    } else if (_y1 > (64 - 32)) {
      dy = -1;
    }

    _x1 += dx;
    _y1 += dy;

    DrawBitMap(fb, _x1, _y1);
    fb.show();
  }

  return 0;
}